numbers = [1, 2, 3, 4, 5]
numbers.delete_at(1)

puts numbers

# delete_at will delete the item in the position specified
# which in this case is position 1 so the number
# 2 will be deleted

numbers = [1, 2, 3, 4, 5]
numbers.delete(1)

puts numbers
# delete will delete the item specified so in this case
# it will delete the number 1 which is at position 0 in the array
